// Этап 2 BIOS-загрузчика. Меню/логика
// stage2.c — Этап 2 BIOS-загрузчика
// Решение: вывод текста и подготовка к загрузке ядра (будет позже)

__attribute__((section(".text"))) void main() {
    const char* text = "Этап 2 успешно загружен. Добро пожаловать!";
    char* video_memory = (char*)0xB8000; // Адрес видеопамяти (текстовый режим)

    // Выводим строку на экран
    for (int i = 0; text[i] != 0; ++i) {
        video_memory[i * 2] = text[i];     // Символ
        video_memory[i * 2 + 1] = 0x0F;    // Цвет: белый на чёрном
    }

    // Бесконечный цикл
    for (;;);
}
